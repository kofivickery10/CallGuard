import { useState } from 'react';
import { useQuery, useMutation, useQueryClient } from '@tanstack/react-query';
import { Link } from 'react-router-dom';
import { api } from '../api/client';
import { useAuth } from '../context/AuthContext';
import { hasFeature } from '@callguard/shared';
import type { InsightDigest, InsightPriority } from '@callguard/shared';

const PERIOD_OPTIONS = [
  { days: 7, label: '7 days' },
  { days: 30, label: '30 days' },
  { days: 90, label: '90 days' },
];

const PRIORITY_STYLES: Record<InsightPriority, string> = {
  critical: 'bg-fail-bg text-fail border-fail',
  high: 'bg-review-bg text-review border-review',
  medium: 'bg-primary-light text-pass border-pass',
  info: 'bg-table-header text-text-secondary border-border',
};

const PRIORITY_LABELS: Record<InsightPriority, string> = {
  critical: 'Critical',
  high: 'High',
  medium: 'Medium',
  info: 'Info',
};

export function AIInsights() {
  const { user } = useAuth();
  const queryClient = useQueryClient();
  const [selectedId, setSelectedId] = useState<string | null>(null);
  const [days, setDays] = useState(7);

  const hasInsights = user ? hasFeature(user.organization_plan, 'insights') : false;

  const { data: digestsData, isLoading } = useQuery({
    queryKey: ['insights'],
    queryFn: () => api.get<{ data: InsightDigest[] }>('/insights'),
    enabled: hasInsights,
  });

  const generate = useMutation({
    mutationFn: () => api.post<InsightDigest>('/insights/generate', { days }),
    onSuccess: (digest) => {
      queryClient.invalidateQueries({ queryKey: ['insights'] });
      setSelectedId(digest.id);
    },
  });

  const digests = digestsData?.data || [];
  const activeDigest = selectedId
    ? digests.find((d) => d.id === selectedId)
    : digests[0];

  if (!hasInsights) {
    return (
      <div>
        <div className="mb-7">
          <h2 className="text-page-title text-text-primary">AI Insights</h2>
          <p className="text-page-sub text-text-subtle mt-1">
            Strategic compliance brief generated by Claude
          </p>
        </div>
        <div className="bg-white border border-border rounded-card p-10 text-center">
          <div className="text-[32px] mb-2">✨</div>
          <h3 className="text-[18px] font-semibold text-text-primary mb-2">
            AI Insights is a Pro feature
          </h3>
          <p className="text-table-cell text-text-muted max-w-md mx-auto mb-4">
            Get an AI chief-of-staff that turns your call data into a strategic brief every week:
            trends, patterns worth attention, and specific coaching recommendations.
          </p>
          <Link
            to="/settings"
            className="inline-block px-4 py-2 bg-primary hover:bg-primary-hover text-white rounded-btn text-[13px] font-semibold transition-colors"
          >
            Upgrade to Pro
          </Link>
        </div>
      </div>
    );
  }

  return (
    <div>
      <div className="flex items-center justify-between mb-7">
        <div>
          <h2 className="text-page-title text-text-primary">AI Insights</h2>
          <p className="text-page-sub text-text-subtle mt-1">
            Strategic compliance brief generated by Claude
          </p>
        </div>
        <div className="flex items-center gap-2">
          <div className="flex gap-1 bg-white border border-border rounded-btn p-1">
            {PERIOD_OPTIONS.map((opt) => (
              <button
                key={opt.days}
                onClick={() => setDays(opt.days)}
                className={`px-3 py-1.5 rounded-btn text-[12px] font-semibold transition-colors ${
                  days === opt.days
                    ? 'bg-primary-light text-pass'
                    : 'text-text-muted hover:text-text-primary'
                }`}
              >
                {opt.label}
              </button>
            ))}
          </div>
          <button
            onClick={() => generate.mutate()}
            disabled={generate.isPending}
            className="px-4 py-2 bg-primary hover:bg-primary-hover disabled:opacity-60 disabled:cursor-not-allowed text-white rounded-btn text-[13px] font-semibold transition-colors"
          >
            {generate.isPending ? 'Generating...' : 'Generate new insights'}
          </button>
        </div>
      </div>

      {generate.isError && (
        <div className="bg-fail-bg border-l-[3px] border-l-fail rounded-r-lg p-4 mb-6">
          <div className="text-table-cell font-semibold text-fail">
            Failed to generate insights
          </div>
          <div className="text-[12px] text-flag-text mt-1">
            {(generate.error as Error).message}
          </div>
        </div>
      )}

      <div className="grid grid-cols-1 lg:grid-cols-[1fr_280px] gap-4">
        <div>
          {isLoading ? (
            <div className="bg-white border border-border rounded-card p-10 text-center text-text-muted">
              Loading...
            </div>
          ) : !activeDigest ? (
            <div className="bg-white border border-border rounded-card p-10 text-center">
              <div className="text-[32px] mb-2">📊</div>
              <h3 className="text-[16px] font-semibold text-text-primary mb-2">
                No insights yet
              </h3>
              <p className="text-table-cell text-text-muted max-w-md mx-auto">
                Click "Generate new insights" above to produce your first strategic brief
                based on the last {days} days of calls.
              </p>
            </div>
          ) : (
            <DigestView digest={activeDigest} />
          )}
        </div>

        <div>
          <div className="bg-white border border-border rounded-card overflow-hidden">
            <div className="px-4 py-3 border-b border-border">
              <h3 className="text-[13px] font-semibold text-text-primary uppercase tracking-wider">
                History
              </h3>
            </div>
            {digests.length === 0 ? (
              <div className="px-4 py-6 text-[12px] text-text-muted text-center">
                No digests yet
              </div>
            ) : (
              <div className="max-h-[600px] overflow-y-auto">
                {digests.map((d) => (
                  <button
                    key={d.id}
                    onClick={() => setSelectedId(d.id)}
                    className={`w-full text-left px-4 py-3 border-b border-border-light last:border-0 hover:bg-table-header transition-colors ${
                      (activeDigest?.id === d.id) ? 'bg-primary-light' : ''
                    }`}
                  >
                    <div className="text-[13px] font-semibold text-text-primary">
                      {formatRange(d.period_start, d.period_end)}
                    </div>
                    <div className="text-[11px] text-text-muted mt-0.5">
                      {formatDate(d.created_at)}
                    </div>
                  </button>
                ))}
              </div>
            )}
          </div>
        </div>
      </div>
    </div>
  );
}

function DigestView({ digest }: { digest: InsightDigest }) {
  const metrics = digest.metrics as {
    total_calls?: number;
    scored_calls?: number;
    avg_score_current?: number | null;
    pass_rate_current?: number | null;
    corrections_count?: number;
    exemplars_count?: number;
  };

  return (
    <div className="space-y-4">
      {/* Summary card */}
      <div className="bg-white border border-border rounded-card overflow-hidden">
        <div className="px-5 py-4 border-b border-border flex items-center justify-between">
          <div>
            <h3 className="text-[15px] font-semibold text-text-primary">
              {formatRange(digest.period_start, digest.period_end)}
            </h3>
            <div className="text-[11px] text-text-muted mt-0.5">
              Generated {formatDate(digest.created_at)}
            </div>
          </div>
          {digest.model_id && (
            <span className="text-[10px] uppercase tracking-wider text-text-muted bg-table-header px-2 py-1 rounded">
              {digest.model_id}
            </span>
          )}
        </div>

        {/* Metrics strip */}
        <div className="grid grid-cols-4 border-b border-border-light">
          <MetricCell label="Calls" value={metrics.total_calls ?? '—'} />
          <MetricCell
            label="Avg score"
            value={metrics.avg_score_current != null ? `${metrics.avg_score_current.toFixed(1)}%` : '—'}
          />
          <MetricCell
            label="Pass rate"
            value={metrics.pass_rate_current != null ? `${metrics.pass_rate_current.toFixed(1)}%` : '—'}
          />
          <MetricCell
            label="Corrections"
            value={metrics.corrections_count ?? 0}
          />
        </div>

        <div className="px-5 py-4">
          <div className="text-table-cell text-text-cell leading-relaxed whitespace-pre-wrap">
            {digest.summary}
          </div>
        </div>
      </div>

      {/* Recommendations */}
      <div className="bg-white border border-border rounded-card overflow-hidden">
        <div className="px-5 py-4 border-b border-border">
          <h3 className="text-[15px] font-semibold text-text-primary">
            Recommendations
          </h3>
        </div>
        <div>
          {digest.recommendations.map((rec, idx) => (
            <div
              key={idx}
              className="px-5 py-4 border-b border-border-light last:border-0"
            >
              <div className="flex items-start gap-3">
                <span
                  className={`flex-shrink-0 px-2 py-[3px] rounded text-[10px] font-bold uppercase tracking-wider border ${
                    PRIORITY_STYLES[rec.priority] || PRIORITY_STYLES.info
                  }`}
                >
                  {PRIORITY_LABELS[rec.priority] || rec.priority}
                </span>
                <div className="flex-1 min-w-0">
                  <div className="text-[14px] font-semibold text-text-primary">
                    {rec.title}
                  </div>
                  <div className="text-table-cell text-text-cell mt-1 leading-relaxed">
                    {rec.detail}
                  </div>
                  {rec.cta && (
                    <Link
                      to={rec.cta.href}
                      className="inline-block mt-2 text-[12px] text-primary hover:text-primary-hover font-semibold"
                    >
                      {rec.cta.label} →
                    </Link>
                  )}
                </div>
              </div>
            </div>
          ))}
        </div>
      </div>
    </div>
  );
}

function MetricCell({ label, value }: { label: string; value: string | number }) {
  return (
    <div className="px-5 py-3 border-r border-border-light last:border-0">
      <div className="text-[10px] uppercase tracking-wider text-text-muted font-semibold">
        {label}
      </div>
      <div className="text-[18px] font-bold text-text-primary font-mono mt-0.5">
        {value}
      </div>
    </div>
  );
}

function formatDate(iso: string): string {
  const d = new Date(iso);
  return d.toLocaleDateString('en-GB', {
    day: 'numeric',
    month: 'short',
    year: 'numeric',
    hour: '2-digit',
    minute: '2-digit',
  });
}

function formatRange(startIso: string, endIso: string): string {
  const start = new Date(startIso);
  const end = new Date(endIso);
  const fmt: Intl.DateTimeFormatOptions = { day: 'numeric', month: 'short' };
  return `${start.toLocaleDateString('en-GB', fmt)} – ${end.toLocaleDateString('en-GB', fmt)}`;
}
